namespace Xamarin.UITest.Android
{
    /// <summary>
    /// User actions that can be sent to android applications
    /// </summary>
    public enum UserAction
	{
        /// <summary>
        /// The normal action.
        /// </summary>
        Normal,
        /// <summary>
        /// The unspecified action.
        /// </summary>
        Unspecified,
        /// <summary>
        /// The none action.
        /// </summary>
        None,
        /// <summary>
        /// The go action.
        /// </summary>
        Go,
        /// <summary>
        /// The search action.
        /// </summary>
        Search,
        /// <summary>
        /// The send action.
        /// </summary>
        Send,
        /// <summary>
        /// The next action.
        /// </summary>
        Next,
        /// <summary>
        /// The done action.
        /// </summary>
        Done,
        /// <summary>
        /// The previous action.
        /// </summary>
        Previous
	}
}
